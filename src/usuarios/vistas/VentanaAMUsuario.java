        Cliente unCliente = new Cliente(correo, clave, apellido, nombre);
        this.clientes.add(unCliente);
        
        System.out.println("Clientes");
        System.out.println("========");
        for(Cliente c : this.clientes) {
            c.mostrar();
            System.out.println();
        }
